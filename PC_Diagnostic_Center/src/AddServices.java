    public ArrayList<Services> servicesList(){
        ArrayList<Services> serviceList = new ArrayList<>();
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1433;databaseName=project_checkpoint2;user=sa;password=123456";
            Connection con = DriverManager.getConnection(url);
            String query1="SELECT * FROM AV_SERVICES";
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery(query1);
            Services services;
            while(rs.next()){
                services=new Services(rs.getInt("id"), rs.getInt("Partsid"), rs.getString("Stype"), rs.getString("EstAmount"));
                serviceList.add(services);
            }
         }
         catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
         return serviceList; 
       
    }
    public void show_service(){
        ArrayList<Services> list = servicesList();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object[] row = new Object[6];
        for(int i=0;i<list.size();i++){
            row[0]=list.get(i).getserviceid();
            row[1]=list.get(i).getpartsid();
            row[2]=list.get(i).gettype();
            row[3]=list.get(i).getamount();
            model.addRow(row);
        }
    }
