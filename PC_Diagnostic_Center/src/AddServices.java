    private void searchfieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchfieldKeyReleased
        // TODO add your handling code here:
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1433;databaseName=project_checkpoint2;user=sa;password=123456";
            Connection con = DriverManager.getConnection(url);
            String sql = "SELECT * FROM AV_SERVICES WHERE id=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,searchfield.getText());
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                //String setid = getString("id");
                idtextfield.setText(rs.getString("id"));
                
                String setproduct = rs.getString("Partsid");
                switch(setproduct){
                    case "305":
                    jComboBox1.setSelectedIndex(0);
                    break;
                    case "306":
                    jComboBox1.setSelectedIndex(1);
                    break;
                    case "307":
                    jComboBox1.setSelectedIndex(2);
                    break;    
                
                }
                
                
                //String setfname = ;
                typefield.setText(rs.getString("Stype"));
                
             //   String setlname = getString("LName");
                amountfield.setText(rs.getString("EstAmount"));
                
                
                
            }
            }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchfieldKeyReleased

